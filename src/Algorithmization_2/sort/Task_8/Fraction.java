package Algorithmization_2.sort.Task_8;

public class Fraction {
    private int p;
    private int q;

    public int getP() {
        return p;
    }

    public void setP(int p) {
        this.p = p;
    }

    public int getQ() {
        return q;
    }

    public void setQ(int q) {
        this.q = q;
    }

    @Override
    public String toString() {
        return p + "/" + q + " ";
    }
}

